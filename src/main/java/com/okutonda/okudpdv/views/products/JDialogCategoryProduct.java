    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        // TODO add your handling code here:

        GroupsProduct cModel = new GroupsProduct();

        if (jTextFieldCode.getText().isEmpty() || jTextFieldCode.getText().length() < 3) {
            JOptionPane.showMessageDialog(null, "Campo Codigo invalido!! no minimo 3 caracteres");
        } else if (jTextFieldName.getText().isEmpty() || jTextFieldName.getText().length() < 3) {
            JOptionPane.showMessageDialog(null, "Campo Descrição invalido!! no minimo 3 caracteres");
        } else {
//            cModel.setId(Integer.parseInt(jTextFieldId.getText()));
            cModel.setCode(jTextFieldCode.getText());
            cModel.setName(jTextFieldName.getText());

            Boolean status = false;
            if (cModel != null) {
                int id = jTextFieldId.getText().isEmpty() == true ? 0 : Integer.parseInt(jTextFieldId.getText());
//            boolean response;
                if (id == 0) {
                    GroupsProduct savedGroup = groupController.save(cModel, 0);
                    status = (savedGroup != null); // Converte para boolean
                } else {
                    GroupsProduct savedGroup = groupController.save(cModel, id);
                    status = (savedGroup != null); // Converte para boolean
                }

                if (status == true) {
                    JOptionPane.showMessageDialog(null, "Categoria salvo com Sucesso!!");
                } else {
                    JOptionPane.showMessageDialog(null, "Error Categoria não salvo!!");
                }
            }

        }
    }//GEN-LAST:event_jButtonSaveActionPerformed

