    public void loadListGroup(List<GroupsProduct> list) {
        DefaultTableModel data = (DefaultTableModel) jTableGroups.getModel();
        data.setNumRows(0);
        for (GroupsProduct c : list) {
            data.addRow(new Object[]{
                c.getId(),
                c.getName(),
                c.getCode(),});
        }
    }

    public void listGroup() {
        List<GroupsProduct> list = groupController.getAll();
        loadListGroup(list);
    }

    public void filterListGroup(String txt) {
//        ProductDao cDao = new ProductDao();
        List<GroupsProduct> list = groupController.filter(txt);
        loadListGroup(list);
    }

