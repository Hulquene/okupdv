    private void jButtonSaveOpenShiftActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveOpenShiftActionPerformed
        // TODO add your handling code here:
        String value = jTextFieldValueOpenShift.getText();
        if (!value.isEmpty() && Util.isDouble(value)) {
            Shift shift = new Shift();
            shift.setGrantedAmount(Double.valueOf(value));
            shift.setIncurredAmount(0.0);
            shift.setClosingAmount(0.0);
            
            shift.setUser(session.getUser());
            Boolean resp = shiftController.openShift(shift);
            if (resp == true) {
                dispose();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Valor invalido!", "Atencao", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonSaveOpenShiftActionPerformed

